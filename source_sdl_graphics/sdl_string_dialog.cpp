/*============================================================================*/
///
///  @brief 	Keyboard handling. Please override function.
///				Handle key events. Also in sub-dialogs for items and menu.
///
///  @param		mod [in] Mode like SHIFT, TAB, CTRL etc.
///  @param		sym [in] Symbol typed.
///
///  @post      Key handled then DIALOG_EVENT_PROCESSED.
///
/*============================================================================*/
Estatus CstringDialog::onButton( SDLMod mod, keybutton sym)
{
	(void)mod;
	switch (sym)
	{
	case KEY_F1:
	case KEY_F2:
	case KEY_F3:
	case KEY_F4:
		setTone( sym-KEY_F1+1);
		switch ( m_layout)
		{
		case LAYOUT_LOCAL:
		case LAYOUT_LOCAL_CAPITAL:
			setLayout( LAYOUT_PINYIN);
			break;
		default:
			break;
		}
		findPinyinSymbols();
		break;

	case KEY_F8:
		m_pinyinString ="";
		m_symbolList.setSymbols( m_pinyinString);
		m_searchString.setText( m_pinyinString);
		invalidate();
		break;

	case KEY_LEFT:
		if ( m_cursor >0)
		{
			m_cursor--;
		}
		invalidate();
		break;

	case KEY_UP:
		m_symbolList.scrollRelative(-60, false);
		break;

	case KEY_DOWN:
		m_symbolList.scrollRelative(60, false);
		break;

	case KEY_F10:
		if ( m_chinese)
		{
			setLayout( LAYOUT_RADICAL);
		}
		break;

	case KEY_F9:
		if ( m_chinese ==true && CFG("handwriting")>0)
		{
			setLayout( LAYOUT_HANDWRITING);
			m_handWriter.clearImage();
			invalidate();
		}
		break;

	case KEY_RIGHT:
		if ( m_cursor <=(int)m_string.length())
		{
			m_cursor++;
			invalidate();
		}
		break;

	case KEY_PAGEDOWN:
		m_symbolList.scrollRelative( 3, true);
		break;

	case KEY_PAGEUP:
		m_symbolList.scrollRelative( -3, true);
		break;
	case KEY_SHIFT_F1:
	case KEY_SHIFT_F2:
		invalidate();
		break;

	case KEY_F5: // shift
		switch ( m_layout)
		{
		case LAYOUT_LOCAL:
			m_layout =LAYOUT_LOCAL_CAPITAL;
			break;

		case LAYOUT_LOCAL_CAPITAL:
#ifdef KEY_SPECIALS
			m_layout =LAYOUT_LOCAL_SPECIAL;
			break;

		case LAYOUT_LOCAL_SPECIAL:
#endif
			m_layout =LAYOUT_LOCAL;
			break;

		case LAYOUT_PINYIN:
		case LAYOUT_FOOD1:
		case LAYOUT_FOOD2:
		default:
			m_layout =LAYOUT_LOCAL;
			break;
		}
		setLayout( m_layout);
		break;

	case KEY_F6: // pinyin
		switch ( m_layout)
		{
		case LAYOUT_LOCAL:
		case LAYOUT_LOCAL_CAPITAL:
#ifdef KEY_SPECIALS
		case LAYOUT_LOCAL_SPECIAL:
#endif
			m_layout =LAYOUT_PINYIN;
			break;

		case LAYOUT_PINYIN:
		case LAYOUT_FOOD1:
		case LAYOUT_FOOD2:
		default:
			m_layout =LAYOUT_PINYIN;
			break;
		}
		setLayout( m_layout);
		break;

	case KEY_END:
		handleBackspace();
		break;

	case KEY_F7:
		m_pinyinString ="";
		m_searchString.setText( m_pinyinString);
		setLayout( (m_layout==LAYOUT_FOOD1 || !m_chinese)
				    ? LAYOUT_FOOD2:LAYOUT_FOOD1);
		m_symbolList.clear();
		m_radicalList.resetPaintedArea();
		m_symbolList.resetPaintedArea();
		invalidate();
		break;

	case KEY_DELETE:
		if ( m_string.length()>0 )
		{
			utf8string zc(m_string);
			if ( m_cursor>=(int)zc.size() && m_cursor>0)
			{
				m_cursor--;
			}
			zc.erase( m_cursor);
			m_string =zc;
			invalidate();
		}
		break;

	case KEY_BACKSPACE:
		if ( m_string.length()>0 && m_cursor>0)
		{
			utf8string zc(m_string);
			zc.erase( m_cursor-1);
			m_string =zc;
			if ( m_cursor>0)
			{
				m_cursor--;
			}
			invalidate();
		}
		break;
	case KEY_CR:
		m_valid =true;
		m_running =false;
		invalidate();
		break;
	case KEY_ESCAPE:
		m_valid =false;
		m_running =false;
		invalidate();
		break;

	default:
		if ( sym >=' ' && sym<='~')
		{
			handleInput( mod, sym);
			invalidate();
			break;
		}
		if ( sym>=(int)KEY_FOUND1 && sym<=(int)KEY_FOUND142)
		{
			int index =m_symbolList.getScrollIndex( sym);
			std::string s =m_symbolList.getValue( index);
			if ( m_layout ==LAYOUT_FOOD2)
			{
				replaceChinese( s);
			}
			else
			{
				insertChinese( s);
			}
			m_handWriter.clearImage();
		}
		if ( sym>=KEY_MAP_ITEM1 && sym<=KEY_MAP_ITEM1000 && m_layout ==LAYOUT_RADICAL)
		{
			int item =m_radicalList.getScrollIndex( sym);
			if ( item >=0)
			{
				touchRadical( item);
				m_radicalList.setCursor( item);
				invalidate();
				m_radicalList.invalidateAll();
			}
		}
		return DIALOG_EVENT_PROCESSED;
	}
	return DIALOG_EVENT_PROCESSED;
}

/** @brief Backspace does things according to which screen we use. */
void CstringDialog::handleBackspace()
{
	if ( m_layout ==LAYOUT_HANDWRITING)
	{
		m_handWriter.clearImage();
		invalidate();
		return;
	}
	if ( m_pinyinString.length() ==0)
	{
		return;
	}
	m_pinyinString.erase( m_pinyinString.begin()+m_pinyinString.length()-1);
	m_searchString.setText( m_pinyinString);
	switch ( m_layout)
	{
	case LAYOUT_PINYIN:
		findPinyinSymbols();
		m_searchString.setText( m_pinyinString);
		break;

	case LAYOUT_FOOD1:
		findFoodSymbols();
		m_searchString.setText( m_pinyinString);
		break;

	case LAYOUT_RADICAL:
		break;

	case LAYOUT_FOOD2:
		findFoodStrings();
		m_searchString.setText( m_pinyinString);
		break;
	default:
		break;
	}
	m_radicalList.resetPaintedArea();
	m_symbolList.resetPaintedArea();
}

/** @brief Key for input method.
 *  @param c [in] Key to handle.
 */
void CstringDialog::handleInput( SDLMod mod, keybutton c)
{
	utf8string zs( m_string);
	char d;
	switch ( m_layout)
	{
	case LAYOUT_LOCAL_CAPITAL:
		d=toupper((char)c);
		break;
	case LAYOUT_LOCAL:
	case LAYOUT_PINYIN:
	case LAYOUT_FOOD1:
	case LAYOUT_FOOD2:
		d=tolower((char)c);
		break;
	default:
		d=(char)c;
		break;
	}
	if ( mod &(KMOD_LSHIFT | KMOD_RSHIFT))
	{
		d ^=0x20;
	}
	switch ( m_layout)
	{
	case LAYOUT_PINYIN:
		if (d<'a' || d>'z')
		{
			break;
		}
		m_pinyinString +=d;
		findPinyinSymbols();
		if (m_foundSymbols.length() ==0 && m_pinyinString.length()>=2)
		{
			m_pinyinString =d;
			findPinyinSymbols();
		}
		m_searchString.setText( m_pinyinString);
		break;

	case LAYOUT_FOOD1:
		if (d<'a' || d>'z')
		{
			break;
		}
		m_pinyinString +=d;
		findFoodSymbols();
		if (m_foundSymbols.length() ==0 && m_pinyinString.length()>=2)
		{
			m_pinyinString =d;
			findFoodSymbols();
		}
		m_searchString.setText( m_pinyinString);
		break;

	case LAYOUT_RADICAL:
	case LAYOUT_HANDWRITING:
		break;

	case LAYOUT_FOOD2:
		if (d<'a' || d>'z')
		{
			break;
		}
		m_pinyinString +=d;
		findFoodStrings();
		if (m_translations.getRows() ==0)
		{
			m_pinyinString =d;
			findFoodStrings();
		}
		m_searchString.setText( m_pinyinString);
		break;

	default:
	//case LAYOUT_LOCAL_CAPITAL:
	//case LAYOUT_LOCAL:
	//case LAYOUT_LOCAL_SPECIAL:
		if ( m_cursor >=m_max_string_width)
		{
			break;
		}
		zs.insert( m_cursor++, d);
		m_string =zs;
		break;
	}
}

/** @brief Init the string dialog.
 *  @param string [in] Default value before starting the dialog.
 *  @param flags [in] Some behaviour definition.
 *  @return true on success.
 */
bool CstringDialog::onInit( const std::string &string, int flags)
{
	m_pinyinString ="zhong";
	m_searchString.setText( m_pinyinString);
	findPinyinSymbols();
	utf8string zs(string);
	m_cursor =zs.size();
	m_string =string;
	m_valid =false;
	m_local =(flags & STRING_EDIT_LOCAL) ? true:false;
	m_chinese =(flags & STRING_EDIT_CHINESE) ? true:false;
	m_layout =LAYOUT_LOCAL;
	if ( !m_chinese)
	{
		m_radical.setVisible( false);
		m_pinyin.setVisible( false);
		m_hand.setVisible( false);
		m_food.setVisible( true);
	}
	if ( !m_local)
	{
		m_shift.setVisible( false);
		m_layout =LAYOUT_PINYIN;
	}
	if ( m_local ==false && m_chinese ==false)
	{
		Log.error( "CstringDialog::onInit  No language enabled!!");
	}
	//DarkenAT(0,0, SQUARES_WIDTH, SQUARES_HEIGHT);
	m_spaceIsLanguage =false;
	setLayout( m_layout);
	return true;
}

