	{
		m_handWriter.onDrag( p);
		CdialogObject *dd=dynamic_cast<CdialogObject*>( &m_handWriter);
		if (dd) dd->onPaint(0);
		m_graphics->update( m_handWriter.left(), m_handWriter.top(), m_handWriter.width(), m_handWriter.height());
	}
